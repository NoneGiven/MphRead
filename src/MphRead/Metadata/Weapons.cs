        {
            // todo: on top of being dumb, this only works for AMHP
            if (address == 0x20BDAB0)
            {
                return Ricochets[0];
            }
            if (address == 0x20CA130)
            {
                return Ricochets[1];
            }
            if (address == 0x20CA220)
            {
                return Ricochets[2];
            }
            if (address == 0x20CA040)
            {
                return Ricochets[3];
            }
            return null;
        }

