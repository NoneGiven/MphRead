    [Flags]
    public enum WeaponFlags : uint
    {
        None = 0x0,
        // bits 0-7 are the priority value
        PartialCharge = 0x100,
        CanCharge = 0x200,
        RepeatFire = 0x400,
        CanZoom = 0x800,
        RicochetUncharged = 0x1000,
        RicochetCharged = 0x2000,
        AutoRelease = 0x4000, // unused - fires charge shot automatically if player/enemy is within 1 unit?
        SelfDamageUncharged = 0x8000,
        SelfDamageCharged = 0x10000,
        ForceEffectUncharged = 0x20000,
        ForceEffectCharged = 0x40000,
        AoeUncharged = 0x80000,
        AoeCharged = 0x100000,
        Continuous = 0x200000,
        DestroyableUncharged = 0x400000,
        DestroyableCharged = 0x800000,
        RadIdx1Uncharged = 0x1000000, // pair with bit 25: index 0-3 of uncharged radius for enemy beam collision with player beams
        RadIdx2Uncharged = 0x2000000,
        RadIdx1Charged = 0x4000000, // pair with bit 27: index 0-3 of charged radius for enemy beam collision with player beams
        RadIdx2Charged = 0x8000000,
        LifeDrainUncharged = 0x10000000,
        LifeDrainCharged = 0x20000000,
        SurfaceCollision = 0x40000000
    }

